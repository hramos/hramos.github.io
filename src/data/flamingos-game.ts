// Play-by-play data for Serpientes @ Flamingos, May 16 2026.
// Each play encodes the field state AFTER the at-bat (or runner event) completes.

export type Half = 'top' | 'bottom'
export type Bases = readonly [boolean, boolean, boolean] // [1B, 2B, 3B]

export type PlayResult =
	| 'walk'
	| 'single'
	| 'double'
	| 'triple'
	| 'home_run'
	| 'strikeout'
	| 'groundout'
	| 'flyout'
	| 'popout'
	| 'doubleplay'
	| 'caught_stealing'
	| 'inning_ended' // scorekeeper-called end of half

export interface Play {
	inning: number
	half: Half
	batter: string
	result: PlayResult
	summary: string
	outs: number // outs AFTER this play (0-3)
	runs: number // runs scored ON this play
	bases: Bases // bases occupied AFTER this play
	away: number // cumulative away score after this play
	home: number // cumulative home score after this play
	scorers?: string[] // names of runners who scored on this play (length === runs)
}

export interface Game {
	awayTeam: string
	awayShort: string
	homeTeam: string
	homeShort: string
	date: string
	location?: string
	innings: number
	awayLine: (number | null)[]
	homeLine: (number | null)[]
	totals: {
		away: { R: number; H: number; E: number }
		home: { R: number; H: number; E: number }
	}
	plays: Play[]
}

